"use strict";

// 1-st approach
function makeArray(firstArray, secondArray, maxLength) {
  const firstArrayString = String(firstArray);
  const secondArrayString = String(secondArray);
  const fullArrayString = firstArrayString + "," + secondArrayString;
  // console.log(fullArrayString);
  const fullArray = fullArrayString.split(",");
  // console.log(fullArray);
  const newArray = fullArray.slice(0, maxLength);
  return newArray;
}

console.log(makeArray(["Mango", "Poly"], ["Ajax", "Chelsea"], 3)); // ["Mango", "Poly", "Ajax"]
console.log(makeArray(["Mango", "Poly", "Houston"], ["Ajax", "Chelsea"], 4)); // ["Mango", "Poly", "Houston", "Ajax"]
console.log(makeArray(["Mango"], ["Ajax", "Chelsea", "Poly", "Houston"], 3)); // ["Mango", "Ajax", "Chelsea"]
console.log(makeArray(["Earth", "Jupiter"], ["Neptune", "Uranus"], 2)); // ["Earth", "Jupiter"]
console.log(makeArray(["Earth", "Jupiter"], ["Neptune", "Uranus"], 4)); // ["Earth", "Jupiter", "Neptune", "Uranus"]
console.log(makeArray(["Earth", "Jupiter"], ["Neptune", "Uranus", "Venus"], 0)); // []

// // 2-nd approach
function makeArray(firstArray, secondArray, maxLength) {
  const fullArray = firstArray.concat(secondArray);
  // console.log(fullArray);

  const newArray = fullArray.slice(0, maxLength);
  return newArray;
}

console.log(makeArray(["Mango", "Poly"], ["Ajax", "Chelsea"], 3)); // ["Mango", "Poly", "Ajax"]
